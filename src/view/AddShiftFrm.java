    private void btnSearchShiftActionPerformed(ActionEvent evt) {//GEN-FIRST:event_btnSearchShiftActionPerformed
        // TODO add your handling code here:
        lblErrorWorkingDate.setText(" ");
        lblErrorStartTime.setText(" ");
        lblErrorEndTime.setText(" ");

        boolean isValid = true;

        LocalDate workingDate = pkrWorkingDate.getDate();
        if (workingDate == null) {
            lblErrorWorkingDate.setText("*Vui lòng nhập ngày làm việc");
            isValid = false;
        }

        LocalTime startTime = pkrStartTime.getTime();
        if (startTime == null) {
            lblErrorStartTime.setText("*Vui lòng nhập giờ bắt đầu");
            isValid = false;
        }

        LocalTime endTime = pkrEndTime.getTime();
        if (endTime == null) {
            lblErrorEndTime.setText("*Vui lòng nhập giờ kết thúc");
            isValid = false;
        }
//
        if (startTime != null && endTime != null && !endTime.isAfter(startTime)) {
            lblErrorStartTime.setText("*Giờ kết thúc phải sau giờ bắt đầu");
            lblErrorEndTime.setText("*Giờ kết thúc phải sau giờ bắt đầu");
            isValid = false;
        }

        if (!isValid) {
            return;
        }

        Shift shift = new Shift(0, workingDate, startTime, endTime);
        ShiftDAO shiftDAO = new ShiftDAO();
        this.shifts = shiftDAO.searchShift(shift);

        DefaultTableModel tableModel = (DefaultTableModel) tblShift.getModel();
        tableModel.setRowCount(0);

        int cnt = 0;
        for (Shift s : this.shifts) {
            tableModel.addRow(new Object[]{
                    ++cnt,
                    s.getWorkingDate().format(dateFormatter),
                    s.getStartTime().format(timeFormatter),
                    s.getEndTime().format(timeFormatter),
                    false
            });
        }
    }//GEN-LAST:event_btnSearchShiftActionPerformed

    private void btnAddShiftActionPerformed(ActionEvent evt) {//GEN-FIRST:event_btnAddShiftActionPerformed
        // TODO add your handling code here:
        String requiredWorkerString = txtRequiredWorker.getText();
        String agreedWageString = txtAgreedWage.getText();

        lblErrorRequiredWorker.setText(" ");
        lblErrorAgreeWage.setText(" ");

        boolean isValid = true;

        int requiredWorkers = 0;
        if (requiredWorkerString.isEmpty()) {
            lblErrorRequiredWorker.setText("*Vui lòng nhập số nhân công");
            isValid = false;
        } else {
            try {
                requiredWorkers = Integer.parseInt(requiredWorkerString);
                if (requiredWorkers <= 0) {
                    lblErrorRequiredWorker.setText("*Số nhân công phải lớn hơn 0");
                    isValid = false;
                }
            } catch (Exception e) {
                lblErrorRequiredWorker.setText("*Số nhân công phải là số nguyên");
                isValid = false;
            }
        }

        double agreedWage = 0;
        if (agreedWageString.isEmpty()) {
            lblErrorAgreeWage.setText("*Vui lòng nhập đơn giá");
            isValid = false;
        } else {
            try {
                agreedWage = Double.parseDouble(agreedWageString);
                if (agreedWage <= 0) {
                    lblErrorAgreeWage.setText("*Đơn giá phải lớn hơn 0");
                    isValid = false;
                }
            } catch (Exception e) {
                lblErrorAgreeWage.setText("*Đơn giá phải là số");
                isValid = false;
            }
        }

        if (!isValid) {
            return;
        }

        DefaultTableModel tableModelShifts = (DefaultTableModel) tblShift.getModel();
        DefaultTableModel tableModelContractJobShifts = (DefaultTableModel) tblContractJobShift.getModel();

        ArrayList<ContractJobShift> existedShifts = new ArrayList<>();
        ArrayList<ContractJobShift> selectedShifts = new ArrayList<>();

        for (int i = 0; i < tableModelShifts.getRowCount(); ++i) {
            if ((boolean) tableModelShifts.getValueAt(i, 4)) {
                ContractJobShift selectedShift = new ContractJobShift(
                        i,
                        requiredWorkers,
                        agreedWage,
                        this.shifts.get(i)
                );

                selectedShifts.add(selectedShift);
                if (this.contractJobShifts.contains(selectedShift)) {
                    tableModelShifts.setValueAt(false, i, 4);
                    existedShifts.add(selectedShift);
                }
            }
        }

        if (selectedShifts.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ít nhất một ca làm để thêm.", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        } else if (existedShifts.isEmpty()) {
            for (ContractJobShift cjs : selectedShifts) {
                tableModelShifts.setValueAt(false, cjs.getId(), 4);

                this.contractJobShifts.add(cjs);
                tableModelContractJobShifts.addRow(new Object[]{
                        tableModelContractJobShifts.getRowCount() + 1,
                        cjs.getShift().getWorkingDate().format(dateFormatter),
                        cjs.getShift().getStartTime().format(timeFormatter),
                        cjs.getShift().getEndTime().format(timeFormatter),
                        cjs.getRequiredWorkers(),
                        cjs.getAgreedWage()
                });
            }
        } else {
            String message = "Ca làm số " + existedShifts.stream().map(cjs -> String.valueOf(cjs.getId() + 1)).collect(Collectors.joining(", ")) + " đã được thêm trước đó.";
            JOptionPane.showMessageDialog(this, message, "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddShiftActionPerformed

    private void btnSaveActionPerformed(ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (this.contractJobShifts.isEmpty()) {
            if (JOptionPane.showConfirmDialog(this, "Xác nhận không thêm ca làm cho đầu việc này?\nĐầu việc này sẽ bị xoá.", "Xác nhận", JOptionPane.YES_NO_OPTION) == 0) {
                this.contract.getContractJobs().remove(this.contractJob);
                this.createContractFrm.reloadTblContractJob();
                this.dispose();
            }
            return;
        } else if (!this.contract.getContractJobs().contains(this.contractJob)) {
            this.contract.getContractJobs().add(this.contractJob);
        }

        this.contractJob.setContractJobShifts(this.contractJobShifts);
        this.createContractFrm.reloadTblContractJob();
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

