    private void tblContractJobShiftMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblContractJobShiftMousePressed
        // TODO add your handling code here:
        int row = tblContractJobShift.rowAtPoint(evt.getPoint());

        if (row == -1) {
            return;
        }

        tblContractJobShift.setRowSelectionInterval(row, row);

        if (isRightMouseButton(evt)) {
            JPopupMenu popupMenu = new JPopupMenu();
            JMenuItem itemDelete = new JMenuItem("Xoá");
            popupMenu.add(itemDelete);
            popupMenu.show(tblContractJobShift, evt.getX(), evt.getY());

            itemDelete.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    if (JOptionPane.showConfirmDialog(AddShiftFrm.this, "Bạn có xác nhận xoá ca làm này?", "Xác nhận", JOptionPane.YES_NO_OPTION) == 0) {
                        deleteShiftAtRow(row);
                    }
                }
            });
        }
    }//GEN-LAST:event_tblContractJobShiftMousePressed

    private void deleteShiftAtRow(int row) {
        this.contractJobShifts.remove(row);
        reloadTblContractJobShift();
    }

